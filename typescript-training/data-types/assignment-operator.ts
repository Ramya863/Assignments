// Assignment Operators in TypeScript

// Basic Assignment (=)
let x: number = 10;
let name: string = "John";

// Arithmetic Assignment Operators
let a: number = 20;
a += 5;  // Addition assignment: a = a + 5 (25)
console.log("addition of a:" + a); 
a -= 3;  // Subtraction assignment: a = a - 3 (22)
console.log("subtraction of a:" + a); 
a *= 2;  // Multiplication assignment: a = a * 2 (44)
console.log("multiplication of a:" + a); 
a /= 4;  // Division assignment: a = a / 4 (11)
console.log("division of a:" +  a); 
a %= 3;  // Modulus assignment: a = a % 3 (2)
console.log("modulus of a:" + a); 