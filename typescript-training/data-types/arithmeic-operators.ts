// Arithmetic Operators in TypeScript

let a: number = 10;
let b: number = 3;

// Addition
let addition: number = a + b;
console.log(`Addition: ${a} + ${b} = ${addition}`); // 13

// Subtraction
let subtraction: number = a - b;
console.log(`Subtraction: ${a} - ${b} = ${subtraction}`); // 7

// Multiplication
let multiplication: number = a * b;
console.log(`Multiplication: ${a} * ${b} = ${multiplication}`); // 30

// Division
let division: number = a / b;
console.log(`Division: ${a} / ${b} = ${division}`); // 3.333...

// Modulus (remainder)
let modulus: number = a % b;
console.log(`Modulus: ${a} % ${b} = ${modulus}`); // 1

// Increment
let x2: number = 10;
console.log(++x2); // 11
console.log(x2);

// Decrement
let y2: number = 5;
console.log(--y2); // 4
console.log(y2); 

//post increment
let x1: number = 5;
console.log(x1++); // 5
console.log(x1); // 6       

//post decrement
let y1: number = 5;
console.log(y1--); // 5
console.log(y1); // 4
